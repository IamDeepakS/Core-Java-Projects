    private void btnUpdateSalary1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateSalary1ActionPerformed
        // TODO add your handling code here:
        int basicsalary = Integer.parseInt(txtSalary3.getText());
        int overtime = Integer.parseInt(txtOvertime.getText());
        int bonus = Integer.parseInt(txtBonus.getText());
        int advance = Integer.parseInt(txtAdvance.getText());
        int absent = Integer.parseInt(txtAbsent.getText());

        double hour = 8;
        double days = 30;
        double rate = basicsalary/days/hour;
        double netovertime = overtime*rate;

        double increase = basicsalary + overtime + bonus;
        double decrease =  advance + absent;
        double netSalary = increase - decrease;
        lblOvertime.setText(String.valueOf(netovertime));
        lblRate.setText(String.valueOf(rate));
        lblDeduction.setText(String.valueOf(decrease));
        lblAllowance.setText(String.valueOf(increase));
        lblNetSalary.setText(String.valueOf(netSalary));
    }//GEN-LAST:event_btnUpdateSalary1ActionPerformed

